// let,const & var

// Here we will discuss about the types of variables:
// There are 3 types which includes:
// let
// const
// var

// let => let is used when we are supposed to change the value of varibale i.e:
let burger = "cheese";
burger = "tomato";
console.log(burger);
// Here we can change the value of variables

// const => const is used when we are not supposed to change the value of varibale
const car = "ford";
car = "toyota";
// if we check in console we will find out that value is not changed of variable and you will get an error

// var => This is same like "let" but var is not advised to use while declaring a variable.
